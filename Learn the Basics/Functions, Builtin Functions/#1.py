"""Functions, Builtin Functions """
# Функции - это блоки кода, которые можно вызывать повторно.
# Они позволяют разбить программу на более мелкие, более управляемые части.
# Аргументы - это значения, которые передаются функции при ее вызове.
# Аргументы могут быть любого типа данных.
# Встроенные функции - это функции, которые уже доступны в Python без необходимости их определения.
# Они предоставляют различные полезные функции, такие как print(), len(), int() и т. д.
# Параметр в функции - это переменная, которая передается в функцию при ее вызове. Параметры
# позволяют передавать данные в функцию, чтобы она могла их использовать для выполнения своих задач.


'''
# def function_name(arguments):
#     # блок кода функции
#     return value
# 
# function_name - это имя функции.
# arguments - это список аргументов функции.
# block of code - это блок кода функции.
# return value - это возвращаемое значение функции.
'''


# Вызов функций
# function_name(arguments)

def hello_world():
    print("Hello, world!")


hello_world()

