# Python: Функции и их вызов
# Функции — одна из ключевых конструкций в программировании.
# Без них невозможно сделать практически ничего.
# Знакомство с ними нужно начинать как можно раньше,
# так как весь дальнейший материал оперирует функциями по максимум

# Функция len() считает количество символов в строке. Ниже пример ее вызова:
# Вызов функции len с параметром 'Hello!'
result = len('Hello!')
print(result)  # => 6

# 2
# Python: Сигнатура функции
# Функция pow() всегда имеет два обязательных параметра,
# поэтому ее невозможно вызвать с другим количеством параметров.
# Функция hex() преобразует целое число в шестнадцатеричную строку с префиксом 0x.
print(hex(2000000000000))
# 3
# Python: Аргументы по умолчанию
# Рассмотрим функцию round(), которая округляет переданное ей число:

result = round(10.25, 0)  # 10.0
print(result)
# округление до одного знака после запятой
result = round(10.25, 1)  # 10.2
print(result)
# 4
# Python: Вызов функции — выражение
name = 'python'
# Индексы начинаются с нуля
# Вызов функции и вычитание вместе!
last_index = len(name) - 1
print(last_index)  # => 5
