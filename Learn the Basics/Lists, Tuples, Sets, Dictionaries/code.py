def print_elements(elements):
    for element in elements:
        print(element)


# Списки
my_list = [1, 2, 3, 4, 5]
print_elements(my_list)

# Кортежи
my_tuple = (1, 2, 3, 4, 5)
print_elements(my_tuple)

# Множества
my_set = {1, 2, 3, 4, 5}
print_elements(my_set)

# Словари
my_dict = {'one': 1, 'two': 2, 'three': 3}
print_elements(my_dict.keys())
